//FOR
//Contagem progressiva
console.log('Contagem com FOR')
console.log('Contagem progressiva...')
for(var contador = 0; contador<=10; contador++){
    console.log(contador)
}
console.log('')



//Contagem regressiva
console.log('Contagem progressiva...')
for(var contador = 10; contador>=1; contador--){
    console.log(contador)
}
console.log('')